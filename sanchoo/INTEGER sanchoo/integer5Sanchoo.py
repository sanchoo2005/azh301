# Ввод данных
A = int(input("Введите длину отрезка A (A > B): "))
B = int(input("Введите длину отрезка B (B < A): "))

# Проверка условия
if A <= B or B <= 0:
    print("Некорректные значения A и B")
else:
    # Вычисление длины незанятой части
    remainder = A % B
    print("Длина незанятой части отрезка A:", remainder)